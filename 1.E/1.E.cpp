/*
E. Поразрядная сортировка
Ограничение времени	1 секунда
Ограничение памяти	64Mb
Ввод	стандартный ввод или input.txt
Вывод	стандартный вывод или output.txt

Поразрядная сортировка является одним из видов сортировки, которые работают практически за линейное 
от размера сортируемого массива время. Такая скорость достигается за счет того, что эта сортировка 
использует внутреннюю структуру сортируемых объектов. Изначально этот алгоритм использовался 
для сортировки перфокарт. Первая его компьютерная реализация была создана в университете MIT Гарольдом Сьюардом 
(Harold Н. Seward). Опишем алгоритм подробнее. Пусть задан массив строк s1 , ..., si причём все строки 
имеют одинаковую длину m. Работа алгоритма состоит из m фаз. На i -ой фазе строки сортируются па i -ой 
с конца букве. Происходит это следующим образом. Будем, для простоты, в этой задаче рассматривать строки 
из цифр от 0 до 9. Для каждой цифры создается «корзина» («bucket»), после чего строки si распределяются 
по «корзинам» в соответствии с i-ой цифрой с конца. Строки, у которых i-ая с конца цифра равна j попадают 
в j-ую корзину (например, строка 123 на первой фазе попадет в третью корзину, на второй — во вторую, 
на третьей — в первую). После этого элементы извлекаются из корзин в порядке увеличения номера корзины. 
Таким образом, после первой фазы строки отсортированы по последней цифре, после двух фаз — 
по двум последним, ..., после m фаз — по всем. При важно, чтобы элементы в корзинах сохраняли тот же порядок, 
что и в исходном массиве (до начала этой фазы). Например, если массив до первой фазы имеет вид: 111, 112, 211, 311, 
то элементы по корзинам распределятся следующим образом: в первой корзине будет. 111, 211, 311, а второй: 112. 
Напишите программу, детально показывающую работу этого алгоритма на заданном массиве.

Формат ввода
Первая строка входного файла содержит целое число n (1 ≤ n ≤ 1000) . Последующие n строк содержат каждая 
по одной строке si . Длины всех si , одинаковы и не превосходят 20. Все si состоят только из цифр от 0 до 9.

Формат вывода
В выходной файл выведите исходный массив строк в, состояние «корзин» после распределения элементов 
по ним для каждой фазы и отсортированный массив. Следуйте формату, приведенному в примере.
*/


#include <iostream>
#include <vector>
#include <string>
#include <map>

void /*std::vector<std::string> */radixSort(std::vector<std::string>& s);

int main()
{
	int n;
	std::cin >> n;

	std::vector<std::string> s(n);

	for (int i = 0; i < n; ++i) {
		std::cin >> s[i];
	}

	std::cin.clear();

	std::cout << "Initial array:\n";

	for (int i = 0; i < n; ++i) {
		std::cout << s[i];
		if (i != n - 1) std::cout << ", ";
	}
	std::cout << "\n**********\n";

	radixSort(s);


	std::cout << "Sorted array:\n";
	for (int i = 0; i < n; ++i) {
		std::cout << s[i];
		if (i != n - 1) std::cout << ", ";
	}

}

void /*std::vector<std::string>*/ radixSort(std::vector<std::string>& s)
{
	std::map<char, std::vector<std::string>> b;

	for (int i = s[0].length() - 1; i >= 0; --i) {
		std::cout << "Phase " << s[0].length() - i << '\n';
		for (int j = 0; j < s.size(); ++j) {
			b[s[j][i]].push_back(s[j]);
		}
		for (char c = '0'; c <= '9'; ++c) {
			if (b.find(c) != b.end()) {
				std::cout << "Bucket " << c << ": " << b[c][0];
				for (int k = 1; k < b[c].size(); ++k) {
					std::cout << ", " << b[c][k];
				}
				std::cout << '\n';
			}
			else {
				std::cout << "Bucket " << c << ": " << "empty \n";
			}
		}
		std::cout << "**********\n";

		s.clear();
		for (auto k = b.begin(); k != b.end(); ++k) {
			for (int l = 0; l < k->second.size(); ++l) {
				s.push_back(k->second[l]);
			}
		}

		b.clear();
	}

	//return std::string();
}
